/**
 * Forge the contents of an AGENTS.md from a preamble and ordered modules.
 *
 * Pure: takes strings, returns a string. File I/O lives in bin/cli.js so this
 * stays trivially testable.
 *
 * @param {object}   opts
 * @param {string}   opts.preamble  Intro text placed first.
 * @param {string[]} opts.modules   Rule modules, emitted in array order.
 * @param {string}   opts.source    Source identifier (repo URL) for the header.
 * @param {string}   [opts.commit]  Source commit hash; recorded in the header when given.
 * @param {string}   [opts.date]    Source commit date; recorded alongside the commit.
 * @returns {string} Full AGENTS.md text, newline-terminated.
 */
export function generate({ preamble, modules = [], source, commit, date }) {
  const header = [
    "<!-- GENERATED FILE: DO NOT EDIT.",
    `     Generated by agentsmith from ${source}.`,
    ...(commit ? [`     Source revision ${commit} (${date}).`] : []),
    "     Edit the source instructions and re-run the generator. -->",
  ].join("\n");

  const norm = (m) => (typeof m === 'string' ? { text: m, demote: 1 } : m);
  const blocks = [String(preamble), ...modules.map(norm).map((m) => demoteHeadings(m.text, m.demote))]
    .map((block) => block.trim())
    .filter(Boolean);

  return [header, ...blocks].join("\n\n") + "\n";
}

/**
 * Demote every ATX heading by `by` levels (default 1) so an inlined module's
 * own h1 title nests under the preamble's single h1. Headings are clamped at
 * h6 and never promoted past that.
 *
 * Headings inside fenced code blocks (``` or ~~~) are left untouched.
 *
 * @param {string} markdown  A module's source text.
 * @param {number} [by=1]    Number of levels to demote (must be >= 0).
 * @returns {string} The module with its headings demoted.
 */
export function demoteHeadings(markdown, by = 1) {
  if (by <= 0) return String(markdown);
  let inFence = false;
  return String(markdown)
    .split('\n')
    .map((line) => {
      if (/^\s*(```|~~~)/.test(line)) inFence = !inFence;
      if (inFence) return line;
      return line.replace(/^(#{1,6})(\s)/, (_m, hashes, sp) => '#'.repeat(Math.min(6, hashes.length + by)) + sp);
    })
    .join('\n');
}
